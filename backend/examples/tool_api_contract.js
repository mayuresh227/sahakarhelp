// ====================
// 5. TOOL KEY FORMAT VALIDATION
// ====================
// Regex: /^[a-z0-9_-]+:v\d+$/
// Valid examples:
//   - emi_calculator:v1
//   - pdf_merge:v2
//   - satbara_helper:v1
//   - image_resize:v10

// Invalid examples:
//   - emi_calculator       (missing version)
//   - emi-calculator:v1    (uppercase not allowed)
//   - emiCalculator:v1     (camelCase not allowed)
//   - emi_calculator:1     (version must be v\d+)
//   - emi_calculator:v     (version number required)

