// ====================
// 7. REQUEST ID PROPAGATION
// ====================
/*
requestId flows through:
1. Client sends: X-Request-Id header or generated by server
2. ToolExecutor.execute() - logs with requestId
3. UsageService.checkUsage() - logs with requestId
4. UsageService.consumeCredits() - logs with requestId
5. ExecutionLogger.logStart() - stored in DB
6. Queue job data - includes requestId
7. toolWorker - logs with requestId
8. Response meta - includes requestId

Example log output:
[req_abc123] Usage check: tool=emi_calculator, userId=guest, cost=1
[req_abc123] Consuming credits: tool=emi_calculator, userId=guest, cost=1
[req_abc123] Execution logged: tool=emi_calculator, logId=123
[req_abc123] Job added to queue: jobId=job_123, toolKey=emi_calculator:v1
[req_abc123] Job queued: logId=123, jobId=job_123
[req_abc123] Processing job: jobId=job_123, toolKey=emi_calculator:v1
[req_abc123] Job completed: jobId=job_123, time=45ms
*/

